/**
 * Dictionary shape — derived from {@link en} so missing keys in other
 * locales (`zh`, …) are caught at build time. Leaf values are typed as
 * `string` rather than the precise English literal so non-English
 * translations don't report a "Type '\"取消\"' is not assignable to type
 * '\"Cancel\"'" mismatch on every key.
 */
export type Dictionary = LoosenStrings<typeof en>;

type LoosenStrings<T> = T extends string
	? string
	: T extends readonly (infer U)[]
		? readonly LoosenStrings<U>[]
		: T extends object
			? { [K in keyof T]: LoosenStrings<T[K]> }
			: T;
